#Print 1 to 10
for i in range(1,11):
    print(i)

#Print 1 to 10 with incremet 2
for i in range(1,11,2):     # 1 Start , 11 End , 2 Increment
    print(i)

#Print 10 to 1
for i in range(10,0,-1):
    print(i)

#Print Even Numbers
for i in range(2,11,2):
    print(i)

#Print Reverse Odd Numbers
for i in range(9,0,-2):
    print(i)


