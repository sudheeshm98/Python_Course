A = int(input("Enter First Number: "))
B = int(input("Enter Second Number: "))

#Arithmetic Operators
print("A+B :", A+B)
print("A-B :", A-B)
print("A*B :", A*B)
print("A/B :", A/B)

#Comparisson Operators
print("A==B :",A == B)
print("A<B :",A < B)
print("A>B :",A > B)

#Logical Operators
print("A>6 and A<10 :",A > 6 and B < 10)
print("A>6 or A<10 :",A > 6 or B < 10)
print("not(A>6 and A<10) :",not(A > 6 and B < 10))

